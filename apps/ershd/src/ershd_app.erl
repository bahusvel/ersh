%%%-------------------------------------------------------------------
%% @doc ersh public API
%% @end
%%%-------------------------------------------------------------------

-module(ershd_app).

-behaviour(application).

%% Application callbacks
-export([hello/0, shell/0, start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    io:fwrite("~s\n", [node()]),
    %sleep(),
    %spawn(ershd_app, shell, []),
    ershd_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) -> ok.

%%====================================================================
%% Internal functions
%%====================================================================

shell() ->
    {ok, [X]} = io:fread("$>", "~s"), io:fwrite(X), shell().

hello() -> io:fwrite("Hello\n").
